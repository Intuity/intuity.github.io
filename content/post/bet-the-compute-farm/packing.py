BIN_CAPACITY = 3600 * 1

def pack_bins(runtimes, bin_capacity):
    # Sort in descending order for First-Fit Decreasing
    bins = []
    for runtime in sorted(runtimes, reverse=True):
        for b in bins:
            if sum(b) + runtime <= bin_capacity:
                b.append(runtime)
                break
        else:
            bins.append([runtime])

    return bins

bins = pack_bins(runtimes, BIN_CAPACITY)

print(f"Target bin capacity: {BIN_CAPACITY / 3600:.2f} hours")
print(f"Number of bins used: {len(bins)}")

for i, b in enumerate(bins, 1):
    print(f"Bin {i}: total={sum(b):.2f}s, jobs={len(b)}")

